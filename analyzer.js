    lines.push(`▶ 종합적으로 현재 매출(${d.월매출}만원) 대비 3개월 +5%, 6개월 +10%, 1년 +15% 매출 성장(목표: ${Math.round(d.월매출 * 1.15)}만원) 가능 전망`);
