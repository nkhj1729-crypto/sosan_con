  if (d.경력 > 0) lines.push(`- 대표자는 ${d.업종짧게} 분야에서 약 ${d.경력}년 이상 종사 후 ${창업년도 ? 창업년도[1] + '년' : ''} ${d.업체명}을(를) 개업하여 현재까지 운영 중임.`);
