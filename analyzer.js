function 받침(str) {
  if (!str) return false;
  const last = str.charCodeAt(str.length - 1);
  if (last < 0xAC00 || last > 0xD7A3) return false;
  return (last - 0xAC00) % 28 !== 0;
}

function extractRegion(addr) {
  if (!addr) return '해당 지역';
  const m = addr.match(/(부산|서울|대구|인천|광주|대전|울산|세종|경기|강원|충북|충남|전북|전남|경북|경남|제주)\S*?(시|도)?\s*(\S+?구|\S+?시|\S+?군)\s*(\S+?동|\S+?읍|\S+?면|\S+?로)/);
  if (m) return `${m[1]} ${m[3]} ${m[4]}`.replace(/광역시|특별시|특별자치/g, '');
  return addr.substring(0, 20);
}

function trendAnalysis(values) {
  if (!values || values.length < 6) return '';
  const r6 = values.slice(-6);
  const f3 = (r6[0] + r6[1] + r6[2]) / 3;
  const l3 = (r6[3] + r6[4] + r6[5]) / 3;
  const c = ((l3 - f3) / f3 * 100).toFixed(1);
  const avg = Math.round(l3);
  if (c > 5) return `최근 6개월간 상승 추세(+${c}%), 최근 3개월 평균 ${avg}만원`;
  if (c < -5) return `최근 6개월간 하락 추세(${c}%), 최근 3개월 평균 ${avg}만원`;
  return `최근 6개월간 보합세(${c > 0 ? '+' : ''}${c}%), 최근 3개월 평균 ${avg}만원`;
}

