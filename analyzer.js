function expandSection(sectionName, currentContent, sin, sang, templateId) {
  const 업종 = sin.업종 || sang.분석업종 || '';
  const 업체명 = sin.업체명 || '';
  const 경력 = parseInt(sin.경력) || 0;
  const 종업원 = parseInt(sin.종업원수) || 0;
  const 월매출 = parseInt(sin.월매출액) || 0;
  const 월순이익 = parseInt(sin.월순이익) || 0;
  const 월세 = parseInt(sin.월세) || 0;
  const 지역평균 = parseInt(sang.선택영역_월평균매출) || 0;
  const 경쟁수 = parseInt(sang.선택영역_업소수) || 0;
  const 매출증감 = parseFloat(sang.선택영역_매출증감) || 0;
  const 요청 = sin.요청사항 || '';
  const 업종짧게 = 업종.replace(/[()（）\s·]/g, '').replace(/네일아트포함/g, '').replace(/포함/g, '').trim() || 업종;
  const 순이익률 = 월매출 > 0 ? Math.round(월순이익 / 월매출 * 100) : 0;

  const extra = expandMap[sectionName];
  if (!extra) {
    console.log('[expand] No expand function for: ' + sectionName);
    return currentContent;
  }

  const addition = extra({
    업체명, 업종짧게, 경력, 종업원, 월매출, 월순이익, 월세, 순이익률,
    지역평균, 경쟁수, 매출증감, 요청, sang
  });

  return currentContent + addition;
}

